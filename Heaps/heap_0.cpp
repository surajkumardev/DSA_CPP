//min and max heap 

// 1. Heap

// Complete binary tree (all levels filled except possibly last).

// Satisfies heap property:

// Max-Heap: parent ≥ children

// Min-Heap: parent ≤ children

// Usually implemented as an array.

// 2. Applications of Heap

// Priority Queue (efficiently get highest/lowest priority).

// Heap Sort (O(n log n) time).

// Find K largest or K smallest elements.

// Graph algorithms: Dijkstra, Prim.

// Median maintenance (using min and max heaps).

// 3. Types of Heap

// Max-Heap:

// Parent ≥ children

// Root = maximum element

// Used for maximum priority access

// Min-Heap:

// Parent ≤ children

// Root = minimum element

// Used for minimum priority access

// 4. Min-Heap vs Max-Heap

// Root element: Min-Heap → minimum, Max-Heap → maximum

// Heap property: Min-Heap → parent ≤ children, Max-Heap → parent ≥ children

// Use cases: Min-Heap → get minimum, Max-Heap → get maximum


